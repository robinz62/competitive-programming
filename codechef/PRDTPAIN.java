    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int n = ri();
            int[] a = ril(n);  // sorted
            long ans = 0;
            for (int s = 0; s < n; s++) {
                for (int e = s+2; e < n; e++) {
                    // Binary search for the best "midway" value for j in the middle
                    int i = s;
                    int k = e;
                    int l = s+1;
                    int r = e-1;
                    long best = Long.MIN_VALUE;
                    while (l <= r) {
                        int j = l + (r - l) / 2;
                        long left = a[j] - a[i];
                        long right = a[k] - a[j];
                        best = Math.max(best, left * right);
                        if (left <= right) {
                            l = j+1;
                        } else {
                            r = j-1;
                        }
                    }
                    ans += best;
                }
            }
            pw.println(ans);
        }
    }
