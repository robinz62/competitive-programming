class Pair {
    int fst;
    int snd;
    Pair(int f, int s) {
        fst = f;
        snd = s;
    }
    public int hashCode() {
        return fst * 9991 + snd;
    }
    public boolean equals(Object o) {
        if (!(o instanceof Pair)) return false;
        Pair p = (Pair) o;
        return fst == p.fst && snd == p.snd;
    }
}