    void solve() throws IOException {
        int[] nx = ril(2);
        int n = nx[0];
        int x = nx[1];
        int[][] jumps = new int[n][2];
        for (int i = 0; i < n; i++) jumps[i] = ril(2);
        boolean[][] dp = new boolean[n][20000];
        dp[0][jumps[0][0]] = dp[0][jumps[0][1]] = true;
        for (int i = 1; i < n; i++) {
            for (int prev = 0; prev <= 10000; prev++) {
                if (dp[i-1][prev]) {
                    dp[i][prev+jumps[i][0]] = true;
                    dp[i][prev+jumps[i][1]] = true;
                }
            }
        }
        pw.println(dp[n-1][x] ? "Yes" : "No");
    }
