    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int n = ri();
            char[] s = rs();
            // check if all >
            boolean good = true;
            for (int i = 0; good && i < n; i++) {
                if (s[i] != '>' && s[i] != '-') good = false;
            }
            if (good) {
                pw.println(n);
                continue;
            }
            good = true;
            for (int i = 0; good && i < n; i++) {
                if (s[i] != '<' && s[i] != '-') good = false;
            }
            if (good) {
                pw.println(n);
                continue;
            }

            boolean[] ok = new boolean[n];
            // boolean[] visited = new boolean[n];
            // TreeSet<Integer> q = new TreeSet<>();  // returns good guys
            for (int i = 0; i < n; i++) {
                if (s[i] == '-') {
                    // q.add(i);
                    // q.add((i+1)%n);
                    ok[i] = ok[(i+1)%n] = true;
                    // visited[i] = visited[(i+1)%n] = true;
                    continue;
                }
            }

            int count = 0;
            for (boolean b : ok) if (b) count++;
            pw.println(count);
        }
    }

