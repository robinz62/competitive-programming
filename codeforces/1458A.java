    void solve() throws IOException {
        int[] nm = ril(2);
        int n = nm[0];
        int m = nm[1];
        long[] a = rll(n);
        sort(a);
        long[] b = rll(m);
        if (n == 1) {
            for (long bi : b) {
                pw.print(a[0] + bi + " ");
            }
            pw.println();
            return;
        }
        long g = a[1] - a[0];
        for (int i = 1; i < n; i++) {
            g = gcd(g, a[i] - a[i-1]);
        }
        for (long bi : b) {
            pw.print(gcd(a[0] + bi, g) + " ");
        }
        pw.println();
    }

    public static long gcd(long a, long b) {
        return a == 0 ? b : gcd(b % a, a);
    }

