    void solve() throws IOException {
        int n = ri();
        long eChain = 0;
        long oChain = 0;
        long[] dp = new long[n+5];
        dp[0] = 1;
        long FOUR = modInverseFermat(4, MOD);
        long TWO = modInverseFermat(2, MOD);
        for (int i = 1; i <= n; i++) {
            if (i % 2 == 1) {
                eChain = eChain * FOUR % MOD;
                eChain = (eChain + dp[i-1] * TWO) % MOD;
                dp[i] = eChain;
            } else {
                oChain = oChain * FOUR % MOD;
                oChain = (oChain + dp[i-1] * TWO) % MOD;
                dp[i] = oChain;
            }
        }
        pw.println(dp[n]);
    }

    public static long modPow(long base, long exponent, long m) {
        long ans = 1;
        base = base % m;
        while (exponent > 0) {
            if ((exponent & 1) == 1) ans = (ans * base) % m;
            exponent >>= 1;
            base = (base * base) % m;
        } 
        return ans;
    }

    public static long modInverseFermat(long a, long m) {
        return modPow(a, m - 2, m);
    }

