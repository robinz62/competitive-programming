    void solve() throws IOException {
        int[] abn = ril(3);
        int a = abn[0];
        int b = abn[1];
        int n = abn[2];
        if (a == 1 && Math.pow(a+1, b) >= n) {
            pw.println("Missing");
            return;
        }
        pw.println(dfs(a, b, n) ? "Masha" : "Stas");
    }

    Map<Integer, Map<Integer, Boolean>> memo = new HashMap<>();
    boolean dfs(int a, int b, int n) {
        if (Math.pow(a, b) >= n) return true;
        if (memo.containsKey(a) && memo.get(a).containsKey(b)) return memo.get(a).get(b);
        if (b == 1 && a >= Math.sqrt(n)) {
            return (n-a) % 2 == 0;
        }
        if (!memo.containsKey(a)) memo.put(a, new HashMap<>());
        boolean move1 = dfs(a+1, b, n);
        if (!move1) {
            memo.get(a).put(b, Boolean.TRUE);
            return true;
        }
        boolean move2 = dfs(a, b+1, n);
        if (!move2) {
            memo.get(a).put(b, Boolean.TRUE);
            return true;
        }
        memo.get(a).put(b, Boolean.FALSE);
        return false;
    }

