    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int n = ri();
            List<List<Integer>> adj = new ArrayList<>(n);
            for (int i = 0; i < n; i++) adj.add(new ArrayList<>());
            TreeSet<Integer> q = new TreeSet<>();
            int[] indeg = new int[n];
            for (int i = 0; i < n; i++) {
                int[] prereq = rkil();
                for (int p : prereq) adj.get(p-1).add(i);
                indeg[i] = prereq.length;
                if (indeg[i] == 0) q.add(i);
            }

            int ans = 0;
            while (!q.isEmpty()) {
                List<Integer> next = new ArrayList<>();
                while (!q.isEmpty()) {
                    int u = q.first();
                    q.remove(u);
                    for (int v : adj.get(u)) {
                        indeg[v]--;
                        if (indeg[v] == 0) {
                            if (v > u) q.add(v);
                            else next.add(v);
                        }
                    }
                }
                for (int u : next) q.add(u);
                ans++;
            }
            boolean ok = true;
            for (int x : indeg) if (x != 0) ok = false;
            pw.println(ok ? ans : -1);
        }
    }
