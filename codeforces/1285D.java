    int ans = Integer.MAX_VALUE;
    void dfs(Node curr, int b, int sofar) {
        if (b < 0) {
            ans = Math.min(ans, sofar);
            return;
        }
        if (curr.zero == null) {
            dfs(curr.one, b-1, sofar);
        } else if (curr.one == null) {
            dfs(curr.zero, b-1, sofar);
        } else {
            sofar = sofar | (1 << b);
            dfs(curr.one, b-1, sofar);
            dfs(curr.zero, b-1, sofar);
        }
    }

