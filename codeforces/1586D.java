    void solve() throws IOException {
        int n = ri();
        int[] ans = new int[n+1];
        Arrays.fill(ans, Integer.MIN_VALUE);

        // ? a1 a2 ... an (not necessarily permutation)

        for (int cand = 1; cand <= n; cand++) {
            pw.print("?");
            for (int i = 1; i <= n-1; i++) pw.print(" " + cand);
            pw.print(" " + n);
            pw.println();
            pw.flush();

            int k = ri();
            if (k != 0) {
                ans[n] = cand;
                break;
            }
        }

        if (ans[n] == Integer.MIN_VALUE) ans[n] = n;

        // We want to find index of tgt
        for (int tgt = 1; tgt <= n; tgt++) {
            if (tgt == ans[n]) continue;

            int giveself = tgt;
            int givethem = ans[n];
            pw.print("?");
            for (int i = 1; i <= n-1; i++) pw.print(" " + givethem);
            pw.print(" " + giveself);
            pw.println();
            pw.flush();

            int k = ri();
            ans[k] = tgt;
        }

        pw.print("!");
        for (int i = 1; i <= n; i++) pw.print(" " + ans[i]);
        pw.println();
    }
