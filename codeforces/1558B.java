        int[] nm = ril(2);
        int n = nm[0];
        int m = nm[1];

        int nextRange = 2;
        int[] dp = new int[n+1];
        int[] suffix = new int[n+1];
        dp[n] = suffix[n] = 1;
        for (int i = n-1; i >= 1; i--) {
            dp[i] = suffix[i+1];
            while (i * nextRange <= n) nextRange++;  // probably only happens once
            for (int r = 2; r < nextRange; r++) {
                dp[i] += suffix[i * r];
                if (dp[i] >= m) dp[i] -= m;
                dp[i] += m - ((i+1) * r <= n ? suffix[(i+1) * r] : 0);
                if (dp[i] >= m) dp[i] -= m;
            }
            suffix[i] = suffix[i+1] + dp[i];
            if (suffix[i] >= m) suffix[i] -= m;
        }
