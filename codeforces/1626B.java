    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            char[] s = rs();
            int n = s.length;

            // Find reductions that preserves number of digits
            int idx = -1;
            for (int i = 0; i < n-1; i++) {
                int x = s[i] - '0';
                int y = s[i+1] - '0';
                if (x+y >= 10) idx = i;
            }

            if (idx != -1) {
                int x = s[idx] - '0';
                int y = s[idx+1] - '0';
                int sum = x + y;
                x = sum / 10;
                y = sum % 10;
                s[idx] = (char) (x + '0');
                s[idx+1] = (char) (y + '0');
                pw.println(new String(s));
            } else {
                // All reductions reduce the length.
                s[1] = (char) (s[1] - '0' + s[0] - '0' + '0');
                for (int i = 1; i < n; i++) pw.print(s[i]);
                pw.println();
            }
        }
    }
