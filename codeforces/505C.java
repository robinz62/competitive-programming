    void solve() throws IOException {
        int[] nd = ril(2);
        int n = nd[0];
        int d = nd[1];
        int[] counts = new int[30001];
        for (int i = 0; i < n; i++) counts[ri()]++;

        // want d - magic to map to 0.

        int magic = (int) Math.sqrt(30000) * 2;
        int subtract = d - magic;
        int[][] dp = new int[30001][2 * magic];
        for (int[] row : dp) Arrays.fill(row, -1);
        dp[d][d - subtract] = counts[d];
        int ans = counts[d];
        for (int i = d; i <= 30000; i++) {
            for (int prevIdx = 0; prevIdx < dp[0].length; prevIdx++) {
                int prevJ = prevIdx + subtract;
                int here = dp[i][prevIdx];
                if (here == -1) continue;
                for (int jump = Math.max(1, prevJ-1); jump <= prevJ+1; jump++) {
                    if (i + jump > 30000) continue;
                    int me = here + counts[i + jump];
                    dp[i+jump][jump - subtract] = Math.max(dp[i+jump][jump - subtract], me);
                    ans = Math.max(ans, me);
                }
            }
        }
        pw.println(ans);
    }
