    void solve() throws IOException {
        int n = ri();
        int[] a = ril(n);

        int ans = 0;
        boolean go = true;
        while (go) {
            go = false;
            boolean hasOdd = false;
            for (int i = 0; !hasOdd && i < n; i++) {
                if (a[i] % 2 == 1) hasOdd = true;
            }
            if (!hasOdd) {
                go = true;
                for (int i = 0; i < n; i++) a[i] /= 2;
            }
        }

        int sum = 0;
        int oddIdx = -1;
        for (int i = 0; i < n; i++) {
            sum += a[i];
            if (oddIdx == -1 && a[i] % 2 == 1) oddIdx = i;
        }
        if (sum % 2 != 0) {
            pw.println("0");
            return;
        }

        // Is it even possible? knapsack
        boolean[][] dp = new boolean[n][100*2000+1];
        dp[0][a[0]] = dp[0][0] = true;
        for (int i = 1; i < n; i++) {
            for (int prev = 0; prev <= 100*2000; prev++) {
                if (dp[i-1][prev]) {
                    dp[i][prev] = true;
                    dp[i][prev + a[i]] = true;
                }
            }
        }
        if (sum % 2 == 0 && !dp[n-1][sum / 2]) {
            pw.println("0");
            return;
        }

        pw.println("1");
        pw.println(oddIdx+1);
    }
