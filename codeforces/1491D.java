    void solve() throws IOException {
        int q = ri();
        for (int qi = 0; qi < q; qi++) {
            int[] uv = ril(2);
            int u = uv[0];
            int v = uv[1];
            
            // For any consecutive segment of 1s, we can
            // Scoot the whole thing over by 1
            // Can't increase number of 1s
            if (u > v) pw.println("NO");
            else if (Integer.bitCount(v) > Integer.bitCount(u)) pw.println("NO");
            else {
                Deque<Integer> ubits = new ArrayDeque<>();
                Deque<Integer> vbits = new ArrayDeque<>();
                for (int i = 0; i < 30; i++) {
                    if ((u & (1 << i)) > 0) ubits.addFirst(29-i);
                    if ((v & (1 << i)) > 0) vbits.addFirst(29-i);
                }
                boolean ded = false;
                while (!vbits.isEmpty()) {
                    int vFront = vbits.removeFirst();
                    if (ubits.isEmpty()) {
                        ded = true;
                        break;
                    }
                    int uFront = ubits.removeFirst();
                    while (uFront < vFront) {
                        if (ubits.isEmpty()) {
                            ded = true;
                            break;
                        }
                        uFront = ubits.removeFirst();
                    }
                    if (ded) break;
                }
                pw.println(ded ? "NO" : "YES");
            }
        }
    }
