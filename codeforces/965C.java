    void solve() throws IOException {
        long[] nkMD = rll(4);
        long n = nkMD[0];
        long k = nkMD[1];
        long M = nkMD[2];
        int D = (int) nkMD[3];

        long ans = 0;
        // d is how many times I get x
        for (int d = 1; d <= D; d++) {
            BigInteger[] res = BigInteger.valueOf(n + 1).divideAndRemainder(BigInteger.valueOf(d).multiply(BigInteger.valueOf(k)).add(BigInteger.ONE));
            long xLower = res[1].equals(BigInteger.ZERO) ? res[0].longValue() : res[0].longValue() + 1;
            long xUpper = BigInteger.valueOf(n).divide(BigInteger.valueOf(k).multiply(BigInteger.valueOf(d-1)).add(BigInteger.ONE)).longValue();

            long xtake = Math.min(xUpper, M);
            if (xtake < xLower) continue;
            ans = Math.max(ans, xtake * d);
        }
        pw.println(ans);
    }

