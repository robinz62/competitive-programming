    void solve() throws IOException {
        // dp[i][b] is the number of bits b for numbers up to i.
        int[][] dp = new int[200001][18];
        for (int i = 1; i <= 200000; i++) {
            for (int b = 0; b < 18; b++) {
                dp[i][b] = dp[i-1][b];
                if (((1 << b) & i) > 0) dp[i][b]++;
            }
        }

        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int[] lr = ril(2);
            int l = lr[0];
            int r = lr[1];

            int best = Integer.MAX_VALUE;
            for (int b = 0; b < 18; b++) {
                // Make bit b all ones.
                int countOnes = dp[r][b] - dp[l-1][b];
                best = Math.min(best, r-l+1 - countOnes);
            }

            pw.println(best);
        }
    }
