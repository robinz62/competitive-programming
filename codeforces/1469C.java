    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int[] nk = ril(2);
            int n = nk[0];
            int k = nk[1];
            int[] h = ril(n);

            // lower and upper bounds of where the previous block
            // could possibly be placed
            int lower = h[0];
            int upper = h[0];
            boolean done = false;
            for (int i = 1; i < n-1; i++) {
                // Bad if just placing on ground doesn't get low enough
                if (h[i] >= upper + k) {
                    pw.println("NO");
                    done = true;
                    break;
                }
                // Bad if placing as high as possible doesn't get high enough
                if (h[i] + k-1 + k <= lower) {
                    pw.println("NO");
                    done = true;
                    break;
                }

                // lower is constrained by either current floor or making sure
                // we reach previous fence
                lower = Math.max(h[i], lower+1-k);
                // upper is constrained by either current floor+k-1 limit or
                // making sure we reach previous fence
                upper = Math.min(h[i]+k-1, upper+k-1);
            }

            if (done) continue;
            // Finally, check that placing last block on floor can touch our range
            if (h[n-1] >= lower-k+1 && h[n-1] <= upper+k-1) {
                pw.println("YES");
            } else {
                pw.println("NO");
            }
        }
    }
