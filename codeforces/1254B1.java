    void solve() throws IOException {
        int n = ri();
        int[] a = ril(n);
        int total = 0;
        for (int ai : a) total += ai;
        if (total == 1) {
            pw.println("-1");
            return;
        }
        List<Integer> f = factors(total);
        Collections.sort(f);

        long ans = Long.MAX_VALUE;
        for (int k = 1; k < f.size(); k++) {
            int x = f.get(k);
            long trial = 0;
            List<Integer> curr = new ArrayList<>();
            for (int i = 0; i < n; i++) {
                if (a[i] == 0) continue;
                curr.add(i);
                if (curr.size() == x) {
                    int med = curr.get(curr.size() / 2);
                    for (int j : curr) trial += Math.abs(j - med);
                    curr.clear();
                }
            }
            ans = Math.min(ans, trial);
        }
        pw.println(ans);
    }

    public static List<Integer> factors(int n) {
        List<Integer> factors = new ArrayList<>();
        int i = 1;
        for (; i * i < n; i++) {
            if (n % i == 0) {
                factors.add(i);
                factors.add(n / i);
            }
        }
        if (i * i == n) factors.add(i);
        return factors;
    }

