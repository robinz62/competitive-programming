    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int[] nq = ril(2);
            int n = nq[0];
            int q = nq[1];
            char[] s = rs();

            boolean[] has1L = new boolean[n];
            boolean[] has0L = new boolean[n];
            boolean[] has1R = new boolean[n];
            boolean[] has0R = new boolean[n];
            for (int i = 1; i < n; i++) {
                has1L[i] = has1L[i-1] || s[i-1] == '1';
                has0L[i] = has0L[i-1] || s[i-1] == '0';
            }
            for (int i = n-2; i >= 0; i--) {
                has1R[i] = has1R[i+1] || s[i+1] == '1';
                has0R[i] = has0R[i+1] || s[i+1] == '0';
            }

            for (int qi = 0; qi < q; qi++) {
                int[] lr = ril(2);
                int l = lr[0]-1;
                int r = lr[1]-1;
                if (s[l] == '0' && has0L[l]) {
                    pw.println("YES");
                    continue;
                }
                if (s[l] == '1' && has1L[l]) {
                    pw.println("YES");
                    continue;
                }
                if (s[r] == '0' && has0R[r]) {
                    pw.println("YES");
                    continue;
                }
                if (s[r] == '1' && has1R[r]) {
                    pw.println("YES");
                    continue;
                }
                pw.println("NO");
            }
        }
    }

