    void solve() throws IOException {
        int t = ri();
        for (int ti = 0; ti < t; ti++) {
            int[] xyz1 = ril(3);
            int x1 = xyz1[0];
            int y1 = xyz1[1];
            int z1 = xyz1[2];
            int[] xyz2 = ril(3);
            int x2 = xyz2[0];
            int y2 = xyz2[1];
            int z2 = xyz2[2];

            int len = x1+y1+z1;

            int ans = 0;
            if (z1 >= y2) {
                ans += y2;
                z2 = Math.max(0, z2 - (z1-y2));
                z2 = Math.max(0, z2 - x1);
                ans -= Math.min(z2, y1);
            } else {
                ans += z1;
                z2 = Math.max(0, z2 - x1);
                ans -= Math.min(y1, z2);
            }
            pw.println(ans * 2);
        }
    }

