    void solve() throws IOException {
        int[] nm = ril(2);
        int n = nm[0];
        int m = nm[1];
        char[][] grid = new char[n][];
        for (int i = 0; i < n; i++) grid[i] = rs();

        // X for filled
        // . for empty

        boolean[] bad = new boolean[m];
        for (int j = 1; j < m; j++) {
            for (int i = 1; i < n; i++) {
                if (grid[i-1][j] == 'X' && grid[i][j-1] == 'X') {
                    bad[j] = true;
                    break;
                }
            }
        }

        int[] badprefix = new int[m];
        badprefix[0] = bad[0] ? 1 : 0;
        for (int j = 1; j < m; j++) badprefix[j] = badprefix[j-1] + (bad[j] ? 1 : 0);

        int q = ri();
        for (int i = 0; i < q; i++) {
            int[] x = ril(2);
            x[0]--;
            x[1]--;
            int count = badprefix[x[1]] - (x[0]-1 < 0 ? 0 : badprefix[x[0]-1]);
            if (bad[x[0]]) count--;
            pw.println(count == 0 ? "YES" : "NO");
        }
    }
