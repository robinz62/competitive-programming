    void solve() throws IOException {
        long[] NM = rll(2);
        long N = NM[0];
        int M = (int) NM[1];
        pw.println(helper(N, M));
    }

    Map<Long, Long> memo = new HashMap<>();
    long helper(long N, int M) {
        if (N == M) return 2;
        if (N < 0) return 0;
        if (N < M) return 1;
        if (memo.containsKey(N)) return memo.get(N);
        long ans = 0;
        long mid = N / 2;
        long left = helper(mid, M);  // all in left
        long right = helper(N - mid, M);  // all in right
        ans = (ans + left * right) % MOD;
        // middle two are 0s
        for (int i = 1; i <= M-1; i++) {  // i is how many are in left half
            int j = M-i;
            long l = helper(mid - i, M);
            long r = helper(N-mid - j, M);
            ans = (ans + l * r) % MOD;
        }
        memo.put(N, ans);
        return ans;
    }

