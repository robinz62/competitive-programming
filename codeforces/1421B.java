    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int n = ri();
            char[][] grid = new char[n][];
            for (int i = 0; i < n; i++) grid[i] = rs();
            char x = grid[0][1];
            char y = grid[1][0];
            char a = grid[n-1][n-2];
            char b = grid[n-2][n-1];
            if (x == y && a == b && x == a) {  // all same
                pw.println("2");
                pw.println("1 2");
                pw.println("2 1");
            } else if (x == y && a == b && x != a) {
                pw.println("0");
            } else if (x != y && a == b) {
                pw.println("1");
                if (x == a) pw.println("1 2");
                else pw.println("2 1");
            } else if (x == y && a != b) {
                pw.println("1");
                if (x == a) pw.println(n + " " + (n-1));
                else pw.println((n-1) + " " + n);
            } else if (x != y && a != b && x == a) {
                pw.println("2");
                pw.println("1 2");
                pw.println((n-1) + " " + n);
            } else {
                pw.println("2");
                pw.println("1 2");
                pw.println(n + " " + (n-1));
            }
        }
    }

