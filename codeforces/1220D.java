    void solve() throws IOException {
        int n = ri();
        long[] b = rll(n);
        Map<Integer, Integer> p2Count = new HashMap<>();
        for (long bi : b) {
            int c = Long.numberOfTrailingZeros(bi);
            p2Count.put(c, p2Count.getOrDefault(c, 0) + 1);
        }
        int best = 0;
        int countOfBest = -1;
        for (Map.Entry<Integer, Integer> e : p2Count.entrySet()) {
            int k = e.getKey();
            int c = e.getValue();
            if (c > countOfBest) {
                best = k;
                countOfBest = c;
            }
        }
        pw.println(n - countOfBest);
        for (long bi : b) if (Long.numberOfTrailingZeros(bi) != best) pw.print(bi + " ");
    }

