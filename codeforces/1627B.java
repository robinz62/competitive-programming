    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int[] nm = ril(2);
            int n = nm[0];
            int m = nm[1];

            // nm < 10^5
            int[][] score1 = new int[n][m];
            int[][] score2 = new int[n][m];
            int[][] corners = new int[][]{
                {0, 0}, {0, m-1}, {n-1, 0}, {n-1, m-1}
            };

            for (int i = 0; i < n; i++) {
                for (int j = 0; j < m; j++) {
                    score1[i][j] = Integer.MAX_VALUE;
                    for (int[] corner : corners) {
                        score1[i][j] = Math.min(score1[i][j], Math.abs(i - corner[0]) + Math.abs(j - corner[1]));
                        score2[i][j] = Math.max(score2[i][j], Math.abs(i - corner[0]) + Math.abs(j - corner[1]));
                    }
                }
            }

            List<int[]> byScore = new ArrayList<>();
            for (int i = 0; i < n; i++) for (int j = 0; j < m; j++) byScore.add(new int[]{i, j});
            Collections.sort(byScore, (x, y) -> Integer.compare(score1[x[0]][x[1]], score1[y[0]][y[1]]));
            for (int k = 0; k < n * m; k++) {
                int[] choose = byScore.get(byScore.size()-1 - k);
                pw.print(score2[choose[0]][choose[1]] + " ");
            }
            pw.println();
        }
    }
