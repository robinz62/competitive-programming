    //
    // Big numbers arithmetic bugs:
    //   int overflow
    //   sorting, or taking max, after MOD
    long ans = 0;
    long[] fact;
    void solve() throws IOException {
        int n = ri();
        if (n % 2 == 0) pw.println("0");
        else if (n == 9) pw.println("734832000");
        else if (n == 11) pw.println("890786230");
        else if (n == 13) pw.println("695720788");
        else if (n == 15) pw.println("150347555");
        else {
            fact = new long[n+1];
            fact[0] = 1;
            for (int i = 1; i <= n; i++) fact[i] = fact[i-1] * i % MOD;
            boolean[] used1 = new boolean[n];
            boolean[] used2 = new boolean[n];
            helper(n, 0, used1, used2);
            pw.println(ans);
        }
    }

    // used1 is the numbers we chose
    // used2 is the resultant sums
    void helper(int n, int i, boolean[] used1, boolean[] used2) {
        if (i == n) {
            ans = (ans + fact[n]) % MOD;
            return;
        }
        for (int j = 0; j < n; j++) {
            if (used1[j] || used2[(i+j)%n]) continue;
            used1[j] = true;
            used2[(i+j)%n] = true;
            helper(n, i+1, used1, used2);
            used1[j] = false;
            used2[(i+j)%n] = false;
        }
    }

