    void solve() throws IOException {
        int k = ri();

        long factor = 4l * modInverseFermat(9, MOD) % MOD;

        // dp[i] is the number of colorings for a perfect binary tree with i levels
        long[] dp = new long[k+1];
        dp[1] = 6;
        for (int i = 2; i <= k; i++) {
            long sub = dp[i-1] * dp[i-1] % MOD * factor % MOD;
            dp[i] = 6 * sub % MOD;
        }
        pw.println(dp[k]);
    }
