    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int[] npk = ril(3);
            int n = npk[0];
            int p = npk[1];
            int k = npk[2];
            char[] s = rs();
            int[] xy = ril(2);
            int x = xy[0];
            int y = xy[1];

            // dp[i] is required number of additions if we start on i
            int[] dp = new int[n];
            for (int i = n-1; i >= n-k; i--) {
                if (s[i] == '0') dp[i] = 1;
            }
            for (int i = n-k-1; i >= 0; i--) {
                if (s[i] == '0') dp[i]++;
                dp[i] += dp[i+k];
            }

            // We start on the pth platform
            int ans = Integer.MAX_VALUE;
            for (int i = 0; i + p - 1 < n; i++) {
                ans = Math.min(ans, y * i + x * dp[i + p - 1]);
            }
            pw.println(ans);
        }
    }

