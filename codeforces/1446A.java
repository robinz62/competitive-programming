    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            long[] nW = rll(2);
            int n = (int) nW[0];
            long W = nW[1];
            long[][] w = new long[n][2];
            long[] wx = rll(n);
            for (int i = 0; i < n; i++) {
                w[i][0] = wx[i];
                w[i][1] = i+1;
            }
            Arrays.sort(w, (w1, w2) -> Long.compare(w1[0], w2[0]));

            long M = W % 2 == 0 ? W / 2 : W / 2 + 1;
            // Only consider items with weight <= W
            // If there is single item in the range [M, W], we're already good
            // Otherwise, all elements are less than M.
            int r = n-1;
            while (r >= 0 && w[r][0] > W) r--;
            if (r < 0) {
                pw.println("-1");
                continue;
            }
            if (w[r][0] >= M) {
                pw.println("1");
                pw.println(w[r][1]);
                continue;
            }
            long sum = 0;
            boolean done = false;
            for (int i = 0; i <= r; i++) {
                sum += w[i][0];
                if (sum >= M) {
                    pw.println(i+1);
                    for (int j = 0; j <= i; j++) pw.print((w[j][1]) + " ");
                    pw.println();
                    done = true;
                    break;
                }
            }
            if (!done) {
                pw.println("-1");
            }
        }
    }

