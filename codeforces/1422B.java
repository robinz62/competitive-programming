    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int[] nm = ril(2);
            int n = nm[0];
            int m = nm[1];
            int[][] a = new int[n][];
            for (int i = 0; i < n; i++) {
                a[i] = ril(m);
            }
            int m1 = n / 2;
            int m2 = m / 2;
            long ans = 0;
            for (int i = 0; i < m1; i++) {
                for (int j = 0; j < m2; j++) {
                    int[] nums = new int[4];
                    nums[0] = a[i][j];
                    nums[1] = a[n-1-i][j];
                    nums[2] = a[i][m-1-j];
                    nums[3] = a[n-1-i][m-1-j];
                    Arrays.sort(nums);
                    int mid = nums[1];
                    for (int x : nums) ans += Math.abs(x - mid);
                }
            }

            // handle odd row
            if (n % 2 == 1) {
                int r = n / 2;
                for (int i = 0; i < m2; i++) {
                    ans += Math.abs(a[r][i] - a[r][m-1-i]);
                }
            }
            // handle odd col
            if (m % 2 == 1) {
                int c = m / 2;
                for (int i = 0; i < m1; i++) {
                    ans += Math.abs(a[i][c] - a[n-1-i][c]);
                }
            }
            pw.println(ans);
        }
    }

