    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int[] nm = ril(2);
            int n = nm[0];
            int m = nm[1];
            int[][] rooks = new int[m][];
            boolean[] has = new boolean[n];
            UnionFind uf = new UnionFind(n);
            for (int i = 0; i < m; i++) {
                rooks[i] = ril(2);
                rooks[i][0]--;
                rooks[i][1]--;
                if (rooks[i][0] == rooks[i][1]) continue;
                has[rooks[i][0]] = true;
                uf.union(rooks[i][0], rooks[i][1]);
            }
            Map<Integer, List<Integer>> cc = new HashMap<>();
            for (int i = 0; i < n; i++) {
                if (!cc.containsKey(uf.find(i))) cc.put(uf.find(i), new ArrayList<>());
                cc.get(uf.find(i)).add(i);
            }

            int count = 0;
            for (int r : cc.keySet()) {
                int c = 0;
                for (int x : cc.get(r)) {
                    if (has[x]) c++;
                }
                if (c == cc.get(r).size()) count += c+1;
                else count += c;
            }

            pw.println(count);
        }
    }

