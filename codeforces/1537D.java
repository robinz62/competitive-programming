    Map<Integer, Boolean> memo = new HashMap<>();
    boolean helper(int n) {
        if (memo.containsKey(n)) return memo.get(n);
        List<Integer> factors = factors(n);
        if (factors.size() <= 2) return false;
        for (int f : factors) {
            if (f == 1 || f == n) continue;
            if (!helper(n - f)) {
                memo.put(n, true);
                return true;
            }
        }
        memo.put(n, false);
        return false;
    }

