    void solve() throws IOException {
        int n = ri();
        long[] a = rll(n);
        boolean[] pos = new boolean[n];
        boolean[] flip = new boolean[n];
        pos[n-1] = true;
        long sum = a[n-1];
        for (int i = n-2; i >= 0; i--) {
            if (sum >= a[i]) {
                pos[i] = false;
                sum -= a[i];
            } else {
                pos[i] = true;
                flip[i+1] = true;
                sum = a[i] - sum;
            }
        }
        boolean cumflip = false;
        for (int i = 0; i < n; i++) {
            if (flip[i]) cumflip = !cumflip;
            if (pos[i]) {
                if (cumflip) pw.print("-");
                else pw.print("+");
            } else {
                if (cumflip) pw.print("+");
                else pw.print("-");
            }
        }
        pw.println();
    }
