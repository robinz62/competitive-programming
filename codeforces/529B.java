    void solve() throws IOException {
        int n = ri();
        int[][] a = new int[n][];
        for (int i = 0; i < n; i++) a[i] = ril(2);
        
        int ans = Integer.MAX_VALUE;
        for (int maxH = 1; maxH <= 1000; maxH++) {
            int sumW = 0;
            int canTake = n / 2;
            List<Integer> deltas = new ArrayList<>();
            for (int[] person : a) {
                if (person[0] > maxH && person[1] > maxH) {
                    sumW = 0;
                    break;
                }
                if (person[1] > maxH) {
                    if (canTake == 0) {
                        sumW = 0;
                        break;
                    }
                    canTake--;
                    sumW += person[1];
                } else {
                    // flippable
                    if (person[0] <= maxH) {
                        deltas.add(person[1] - person[0]);
                    }

                    sumW += person[0];
                }
            }

            if (sumW == 0) continue;
            Collections.sort(deltas);
            int D = 0;
            for (int i = 0; i < deltas.size() && deltas.get(i) < 0 && canTake > 0; i++) {
                sumW += deltas.get(i);
                canTake--;
            }

            ans = Math.min(ans, sumW * maxH);
        }
        pw.println(ans);
    }
