    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int n = ri();
            int[] a = ril(n);
            TreeSet<Integer> set = new TreeSet<>();
            for (int ai : a) set.add(Math.abs(ai));
            set.remove(0);
            List<Integer> list = new ArrayList<>(set);

            // n <= 10
            // want array b such that over all diffs, each element of a is represented
            // trivial if |set| < n
            if (list.size() < n) pw.println("YES");
            else {
                boolean ok = false;
                for (int mask = 1; !ok && mask < (1 << n); mask++) {
                    if (Integer.bitCount(mask) == 1) continue;
                    Set<Integer> take = new HashSet<>();
                    for (int b = 0; b < n; b++) if (((1 << b) & mask) > 0) take.add(list.get(b));
                    for (int mask2 = 0; mask2 < (1 << n); mask2++) {
                        if ((mask | mask2) != mask) continue;
                        int sum = 0;
                        for (int b = 0; b < n; b++) {
                            if (((1 << b) & mask) > 0) {
                                if (((1 << b) & mask2) > 0) sum += list.get(b);
                                else sum -= list.get(b);
                            }
                        }
                        if (!take.contains(sum) && set.contains(sum)) ok = true;
                    }
                }
                pw.println(ok ? "YES" : "NO");
            }
        }
    }
