    void solve() throws IOException {
        int n = ri();
        Deque<Integer> q = new ArrayDeque<>();  // idx
        int[] ans = new int[n];
        int[][] queries = new int[2*n][2];
        int cnt = 0;
        for (int i = 0; i < 2*n; i++) {
            char[] line = rs();
            if (line[0] == '+') {
                queries[i][0] = 1;
                q.addLast(cnt++);
            } else {
                queries[i][0] = -1;
                int x = Integer.parseInt(new String(line).trim().split(" ")[1]) - 1;
                queries[i][1] = x;
                if (q.isEmpty()) {
                    pw.println("NO");
                    return;
                }
                int recentIdx = q.removeLast();
                ans[recentIdx] = x;
            }
        }
        TreeSet<Integer> display = new TreeSet<>();
        cnt = 0;
        for (int i = 0; i < 2 * n; i++) {
            if (queries[i][0] == 1) display.add(ans[cnt++]);
            else {
                if (display.isEmpty()) {  // not necessary
                    pw.println("NO");
                    return;
                }

                int min = display.first();
                display.remove(min);
                if (min != queries[i][1]) {
                    pw.println("NO");
                    return;
                }
            }
        }
        pw.println("YES");
        for (int x : ans) pw.print((x+1) + " ");
        pw.println();
    }

