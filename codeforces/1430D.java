    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int n = ri();
            char[] s = rs();
            Deque<Bonk> q = new ArrayDeque<>();
            TreeSet<Bonk> set = new TreeSet<>();
            int count = 1;
            char curr = s[0];
            for (int i = 1; i < n; i++) {
                if (s[i] == curr) count++;
                else {
                    Bonk b = new Bonk(count, i);
                    q.addLast(b);
                    if (count != 1) {
                        set.add(b);
                    }
                    curr = s[i];
                    count = 1;
                }
            }
            Bonk b = new Bonk(count, n);
            if (count != 1) set.add(b);
            q.addLast(b);

            int ans = 0;
            while (!q.isEmpty()) {
                if (!set.isEmpty()) {
                    b = set.first();
                    b.count--;
                    if (b.count == 1) set.remove(b);
                    Bonk r = q.removeFirst();
                    set.remove(r);
                } else {
                    q.removeFirst();
                    if (!q.isEmpty()) q.removeFirst();
                }
                ans++;
            }
            pw.println(ans);
        }
    }

