    void solve() throws IOException {
        int[] nmk = ril(3);
        int n = nmk[0];
        int m = nmk[1];
        int k = nmk[2];
        int[] a = ril(k);

        if (n == k) {
            pw.println("-1");
            return;
        }

        Set<Integer> avoid = new HashSet<>();
        for (int i = 1; i < k; i++) avoid.add(a[i]);

        int unmarked = -1;
        for (int i = 1; i <= n; i++) {
            boolean marked = false;
            for (int ai : a) if (i == ai) marked = true;
            if (!marked) {
                unmarked = i;
                break;
            }
        }

        boolean[][] added = new boolean[n+1][n+1];
        List<int[]> output = new ArrayList<>();
        for (int i = 1; i <= n; i++) {
            if (i == unmarked) continue;
            output.add(new int[]{unmarked, i});
            m--;
            added[unmarked][i] = added[i][unmarked] = true;
        }
        for (int i = 1; m > 0 && i <= n; i++) {
            for (int j = i+1; m > 0 && j <= n; j++) {
                if (i == a[0] && avoid.contains(j) || avoid.contains(i) && j == a[0] || added[i][j]) continue;
                output.add(new int[]{i, j});
                m--;
            }
        }
        if (m > 0) pw.println("-1");
        else {
            for (int[] e : output) pw.println(e[0] + " " + e[1]);
        }
    }
