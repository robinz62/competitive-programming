    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int n = ri();
            int[][] grid = new int[2][];
            grid[0] = ril(n);
            grid[1] = ril(n);
            boolean dead = false;
            for (int i = 0; !dead && i < n; i++) {
                if (grid[0][i] == grid[1][i]) dead = true;
            }
            if (dead) {
                pw.println("0");
                continue;
            }

            for (int i = 0; i < n; i++) {
                grid[0][i]--;
                grid[1][i]--;
            }

            int[] valToFstRowIdx = new int[n];
            int[] valToSndRowIdx = new int[n];
            for (int i = 0; i < n; i++) {
                valToFstRowIdx[grid[0][i]] = i;
                valToSndRowIdx[grid[1][i]] = i;
            }

            UnionFind uf = new UnionFind(n);
            for (int i = 0; i < n; i++) {
                int top = grid[0][i];
                int bot = grid[1][i];

                int other1 = valToSndRowIdx[top];
                int other2 = valToFstRowIdx[bot];

                uf.union(i, other1);
                uf.union(i, other2);
            }

            long ans = 1;
            for (int i = 0; i < uf.numComponents; i++) {
                ans = ans * 2 % MOD;
            }

            pw.println(ans);
        }
    }
