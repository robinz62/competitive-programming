    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int n = ri();
            char[] a = rs();
            char[] b = rs();

            if (new String(a).equals(new String(b))) {
                pw.println("0");
                continue;
            }

            int ans = Integer.MAX_VALUE;

            // Case 1: select positions that match.
            int match0s = 0;
            int match1s = 0;
            for (int i = 0; i < n; i++) {
                if (a[i] == b[i]) {
                    if (a[i] == '0') match0s++;
                    else match1s++;
                }
            }
            int matches = match0s + match1s;
            if (matches % 2 == 0) {
                // Doesn't work since mismatched stays the same
            } else {
                if (match1s == match0s + 1) {
                    ans = Math.min(ans, matches);
                }
            }

            // Case 2: select positions that don't match
            int mismatch0s = 0;
            int mismatch1s = 0;
            for (int i = 0; i < n; i++) {
                if (a[i] != b[i]) {
                    if (a[i] == '0') mismatch0s++;
                    else mismatch1s++;
                }
            }
            int mismatches = mismatch0s + mismatch1s;
            if (mismatches % 2 == 0) {
                if (mismatch0s == mismatch1s) {
                    ans = Math.min(ans, mismatches);
                }
            } else {
                // Doesn't work
            }

            pw.println(ans == Integer.MAX_VALUE ? -1 : ans);
        }
    }
