    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int n = ri();
            int[] a = ril(n);

            int ans = n-1;
            // Find longest arithmetic sequence? including gaps.
            for (int s1 = 0; s1 < n; s1++) {
                for (int s2 = s1+1; s2 < n; s2++) {
                    int difflen = s2 - s1;
                    int d = a[s2] - a[s1];
                    int matches = 2;
                    for (int i = s1-1; i >= 0; i--) {
                        int delta = s1 - i;
                        if (delta * d % difflen != 0) continue;
                        if (a[i] == a[s1] - delta * d / difflen) matches++;
                    }
                    for (int i = s2+1; i < n; i++) {
                        int delta = i - s2;
                        if (delta * d % difflen != 0) continue;
                        if (a[i] == a[s2] + delta * d / difflen) matches++;
                    }
                    ans = Math.min(ans, n - matches);
                }
            }
            pw.println(ans);
        }
    }
