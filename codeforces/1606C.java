    void solve() throws IOException {
        int T = ri();
        long[] pow10 = new long[19];
        pow10[0] = 1;
        for (int i = 1; i <= 18; i++) pow10[i] = pow10[i-1] * 10;
        for (int Ti = 0; Ti < T; Ti++) {
            int[] nk = ril(2);
            int n = nk[0];
            int k = nk[1];
            int[] a = ril(n);  // contains which digits we can use
            boolean[] have = new boolean[10];
            for (int ai : a) have[ai] = true;

            k++;

            long ans = 0;
            int s = 0;
            int o = 1;
            while (k > 0 && o < 10) {
                while (o < 10 && !have[o]) o++;
                if (o >= 10) break;
                int spaces = o - s;
                long fillable = pow10[spaces] - 1;
                long take = Math.min(k, fillable);
                k -= take;
                ans += take * pow10[s];

                s = o;
                o = s+1;
            }
            if (k > 0) ans += k * pow10[s];
            pw.println(ans);
        }
    }
