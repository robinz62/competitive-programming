    void solve() throws IOException {
        int T = ri();
        // Expected for length of n is E[n] = 2 * (2^n - 1)
        long[] E = new long[59];  // E[58] is the biggest one we'll ever need
        for (int i = 1; i <= 58; i++) {
            E[i] = 2l * ((1l << i) - 1);
        }
        // System.out.println(Arrays.toString(E));
        for (int Ti = 0; Ti < T; Ti++) {
            long k = rl();
            // Want to make k using different values of E[i]
            if (k % 2 == 1) {
                pw.println("-1");
                continue;
            }
            List<Integer> ans = new ArrayList<>();
            ans.add(1);
            int i = 58;
            boolean bad = false;
            while (k > 0) {
                while (i > 0 && E[i] > k) i--;
                if (i == 0) {
                    // Shouldn't ever happen...?
                    bad = true;
                    break;
                }

                for (int x = 0; x < i-1; x++) ans.add(0);
                ans.add(1);
                k -= E[i];
            }

            if (bad) continue;
            // Remove the last 1
            ans.remove(ans.size() - 1);
            pw.println(ans.size());
            for (int x : ans) pw.print(x + " ");
            pw.println();
        }
    }

