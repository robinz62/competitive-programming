    void solve() throws IOException {
        int n = ri();
        int[] a = ril(n);
        Node root = new Node(0);
        for (int ai : a) {
            Node curr = root;
            curr.count++;
            for (int i = 29; i >= 0; i--) {
                int bit = (ai & (1 << i)) > 0 ? 1 : 0;
                if (bit == 0) {
                    if (curr.left == null) curr.left = new Node(0);
                    curr = curr.left;
                } else {
                    if (curr.right == null) curr.right = new Node(1);
                    curr = curr.right;
                }
                curr.count++;
            }
        }
        dfs(root, n);
        pw.println(n - ans);
    }

    int ans = 0;
    void dfs(Node root, int have) {
        // leaf
        if (root.left == null && root.right == null) {
            ans = Math.max(ans, have);
            return;
        }

        // go left
        if (root.left != null) {
            int kill = root.right == null ? 0 : root.right.count - 1;
            dfs(root.left, have - kill);
        }
        // go right
        if (root.right != null) {
            int kill = root.left == null ? 0 : root.left.count - 1;
            dfs(root.right, have - kill);
        }
    }

