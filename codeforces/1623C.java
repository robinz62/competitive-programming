    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int n = ri();
            long[] h = rll(n);
            long l = 0;
            long r = 10000000000l;
            long best = 0;
            long[] curr = new long[n];
            while (l <= r) {
                System.arraycopy(h, 0, curr, 0, n);
                long m = l + (r - l) / 2;
                
                // Can we go right to left?
                // Just make sure not to give stones we don't have.
                // (i.e. in realtime, never go below 0)

                for (int i = n-1; i >= 2; i--) {
                    long canGiveReal = h[i] / 3 * 3;     // Upper bound on ability to give based on realtime.
                    long canGiveHave = curr[i] / 3 * 3;  // Upper bound on ability to give based on future returns.
                    long canGiveConstraint = (curr[i] - m) / 3 * 3;  // based on needing to keep >= m)
                    
                    long give = Math.max(0, Math.min(canGiveReal, Math.min(canGiveHave, canGiveConstraint)));
                    long d = give / 3;
                    curr[i] -= 3l * d;
                    curr[i-1] += d;
                    curr[i-2] += 2l * d;
                }

                boolean ok = true;
                for (long x : curr) {
                    if (x < m) {
                        ok = false;
                        break;
                    }
                }
                if (ok) {
                    best = Math.max(best, m);
                    l = m+1;
                } else {
                    r = m-1;
                }
            }
            pw.println(best);
        }
    }
