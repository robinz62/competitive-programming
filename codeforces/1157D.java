    void solve() throws IOException {
        long[] nk = rll(2);
        long n = nk[0];
        int k = (int) nk[1];

        long min = (long) k * (k + 1) / 2;
        if (min > n) {
            pw.println("NO");
            return;
        }

        long[] arr = new long[k];
        for (int i = 0; i < k; i++) arr[i] = i+1;
        long sum = min;
        long increasePer = k;  // If we increarse arr[i] by 1, how much does the sum have to increase?
        long accumX = 0;
        for (int i = 0; i < k; i++) {
            arr[i] += accumX;
            if (sum == n) continue;

            long need = n - sum;

            // X is how much we increase arr[i..] by.
            long X = (n - sum) / increasePer;
            long limit = i == 0 ? Long.MAX_VALUE : arr[i-1] * 2;
            if (arr[i] + X > limit) X = limit - arr[i];

            arr[i] += X;
            accumX += X;
            sum += X * increasePer;
            increasePer--;
        }

        if (sum < n) {
            pw.println("NO");
            return;
        }
        pw.println("YES");
        for (long ai : arr) pw.print(ai + " ");
    }
