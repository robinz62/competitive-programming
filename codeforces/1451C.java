    void solve() throws IOException {
        int T = ri();
        for (int Ti = 0; Ti < T; Ti++) {
            int[] nk = ril(2);
            int n = nk[0];
            int k = nk[1];
            char[] a = rs();
            char[] b = rs();

            int[] fa = new int[26];
            int[] fb = new int[26];
            for (char c : a) fa[c-'a']++;
            for (char c : b) fb[c-'a']++;
            
            boolean bad = false;
            for (int j = 25; !bad && j >= 0; j--) {
                if (fb[j] == 0) continue;
                if (fa[j] == fb[j]) continue;
                if (fa[j] > fb[j]) {  // Too many of biggest in a
                    bad = true;
                    break;
                }
                if ((fb[j] - fa[j]) % k != 0) {
                    bad = true;
                    break;
                }
                int promotionsNeeded = (fb[j] - fa[j]) / k;

                for (int i = j-1; promotionsNeeded > 0 && i >= 0; i--) {
                    if (fa[i] >= k) {
                        int take = Math.min(fa[i] / k, promotionsNeeded);
                        fa[i] -= take * k;
                        fb[j] += take * k;  // not necessary
                        promotionsNeeded -= take;
                    }
                }
                if (promotionsNeeded > 0) {
                    bad = true;
                    break;
                }
            }
            if (bad) pw.println("No");
            else pw.println("Yes");
        }
    }

